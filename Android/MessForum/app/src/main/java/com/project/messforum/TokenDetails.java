package com.project.messforum;

public class TokenDetails {
    int id;
    long regno;
    String name;
    String tname;
    String tnum;
    String toknum;

    public int getId() {
        return id;
    }

    public long getRegno() {
        return regno;
    }

    public String getName() {
        return name;
    }

    public String getTname() {
        return tname;
    }

    public String getTnum() {
        return tnum;
    }

    public String getToknum() {
        return toknum;
    }
}
